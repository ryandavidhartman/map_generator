type RangeEntry = { min: number; max: number; text: string }

function expandD100(entries: RangeEntry[]): string[] {
  const table = new Array<string>(100)
  for (const { min, max, text } of entries) {
    for (let i = min; i <= max; i++) table[i - 1] = text
  }
  return table
}

